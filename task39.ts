function city_countrys(city, country) {
    return `${city}, ${country}`;
}

let result1 = city_countrys('Lahore', 'Pakistan');
let result2 = city_countrys('Tokyo', 'Japan');
let result3 = city_countrys('New York', 'USA');

console.log(result1);
console.log(result2);
console.log(result3);
