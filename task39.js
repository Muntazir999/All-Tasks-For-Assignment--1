function city_countrys(city, country) {
    return "".concat(city, ", ").concat(country);
}
var result1 = city_countrys('Lahore', 'Pakistan');
var result2 = city_countrys('Tokyo', 'Japan');
var result3 = city_countrys('New York', 'USA');
console.log(result1);
console.log(result2);
console.log(result3);
